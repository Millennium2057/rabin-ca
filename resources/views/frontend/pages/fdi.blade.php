                        Foreign Direct Investment in Nepal
                    </p>
                    <p>
                        FDI is regarded as one of the core economic source for the development and economic
                        stability in developing nations globally. Nepal is considered as one of the most liberalized
                        nation amidst other South Asian nations, but the inflow of FDI has not recorded a good hike.
                        Nepal is gifted abundantly with natural resources, favorable weather and cheap labors;
                        however the FDI input is not significant enough. In context of Nepal, the Investment Board
                        is the apex body related to accepting proposals for FDI and is chaired by the Prime Minister
                        of Nepal. Polices and plans related to FDI are developed to make the investor foresee the
                        investment friendliness of the country.
                    </p>
