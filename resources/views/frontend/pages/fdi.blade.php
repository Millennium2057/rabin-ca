                        Significance of Foreign Direct Investment
                    </p>
                    <p>FDI is regarded as one of the finest contributor to boost economy growth. There has been a
                        sweeping change and developments around the globe and many developing nations lack the
                        funding and better understanding of gaining such transition. Investment made in any foreign
                        firm is regarded as a FDI and acquires a lasting interest; a lasting interest is when the
                        investor attains a 10% of voting power in the firm. As discussed, an investor can expand its
                        business in a foreign country and secure exponential growth, for e.g. Daraz (e-commerce
                        website) is a new venture of e-commerce giant Alibaba in Nepal. Reinvesting in existing
                        companies, joint ventures, acquiring stocks or operating a subsidiary can also be enlisted
                        under Foreign Direct Investment.
